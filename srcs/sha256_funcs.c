#include "ft_ssl.h"

uint32_t	sha256ch(uint32_t hash[8])
{
	return ((hash[4] & hash[5]) ^ (~hash[4] & hash[6]));
}

uint32_t	sha256ma(uint32_t hash[8])
{
	return ((hash[0] & hash[1]) ^ (hash[0] & hash[2]) ^ (hash[1] & hash[2]));
}
